class FactoryError(RuntimeError):
    pass


def run(repo: Path, *args: str, check: bool = True) -> subprocess.CompletedProcess[str]:
    env = os.environ.copy()
    env.setdefault("GIT_AUTHOR_DATE", FIXTURE_TIMESTAMP)
    env.setdefault("GIT_COMMITTER_DATE", FIXTURE_TIMESTAMP)
    result = subprocess.run(
        ["git", *args],
        cwd=repo,
        env=env,
        text=True,
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
    )
    if check and result.returncode != 0:
        raise FactoryError(
            f"git {' '.join(args)} failed\nstdout:\n{result.stdout}\nstderr:\n{result.stderr}"
        )
    return result


def git_out(repo: Path, *args: str) -> str:
    return run(repo, *args).stdout.strip()


def write(repo: Path, relative_path: str, content: str) -> None:
    path = repo / relative_path
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")


def commit(repo: Path, role: str, date: str, message: str) -> str:
    name, email = AUTHORS[role]
    env = os.environ.copy()
    env.update(
        {
            "GIT_AUTHOR_NAME": name,
            "GIT_AUTHOR_EMAIL": email,
            "GIT_COMMITTER_NAME": name,
            "GIT_COMMITTER_EMAIL": email,
            "GIT_AUTHOR_DATE": date,
            "GIT_COMMITTER_DATE": date,
        }
    )
    subprocess.run(["git", "add", "."], cwd=repo, check=True, stdout=subprocess.PIPE, stderr=subprocess.PIPE)
    result = subprocess.run(
        ["git", "commit", "-m", message],
        cwd=repo,
        env=env,
        text=True,
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
    )
    if result.returncode != 0:
        raise FactoryError(f"commit failed: {message}\n{result.stdout}\n{result.stderr}")
    return git_out(repo, "rev-parse", "HEAD")


def make_writable_and_retry(function: Any, path: str, excinfo: BaseException) -> None:
    os.chmod(path, stat.S_IWRITE)
    function(path)


def reset_dir(path: Path) -> None:
    if path.exists():
        shutil.rmtree(path, onexc=make_writable_and_retry)
    path.mkdir(parents=True, exist_ok=True)


def available_scenarios() -> list[str]:
    return sorted(path.stem for path in SCENARIOS_DIR.glob("*.yaml"))


def load_scenario(name: str) -> dict[str, Any]:
    path = SCENARIOS_DIR / f"{name}.yaml"
    if not path.exists():
        raise FactoryError(f"unknown scenario '{name}'. Available: {', '.join(available_scenarios())}")

    text = path.read_text(encoding="utf-8")
    try:
        import yaml  # type: ignore

        data = yaml.safe_load(text)
    except ModuleNotFoundError:
        data = json.loads(text)

    if not isinstance(data, dict):
        raise FactoryError(f"scenario file did not contain an object: {path}")
    return data


