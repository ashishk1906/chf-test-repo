def plant_clean_cherry_pick(repo: Path) -> tuple[str, list[str]]:
    run(repo, "checkout", "release/1.0.0")
    write(repo, "src/billing/invoice.py", 'def invoice_total(lines):\n    total = sum(line["amount"] for line in lines)\n    return round(total, 2)\n')
    return commit(repo, "release", "2026-06-20T10:00:00+05:30", "fix(billing): round invoice totals"), []


def plant_conflicting_cherry_pick(repo: Path) -> tuple[str, list[str]]:
    run(repo, "checkout", "release/1.0.0")
    write(repo, "src/auth/session.py", 'def validate_token(token, tenant):\n    if not token:\n        return False\n    normalized = token.strip()\n    return normalized.startswith(f"{tenant}:")\n')
    source_sha = commit(repo, "release", "2026-06-21T10:00:00+05:30", "fix(auth): trim token before validation")
    run(repo, "checkout", "tenant/reliance-v1.0.0")
    write(repo, "src/auth/session.py", 'def validate_token(token, tenant):\n    if not token:\n        return False\n    if tenant == "reliance" and token.startswith("legacy:"):\n        return True\n    return token.startswith(f"{tenant}:")\n')
    commit(repo, "reliance", "2026-06-21T11:00:00+05:30", "fix(auth): allow Reliance legacy token prefix")
    return source_sha, []


def plant_rewritten_history(repo: Path) -> tuple[str, list[str]]:
    run(repo, "checkout", "release/1.0.0")
