def plant_generated_files(repo: Path) -> tuple[str, list[str]]:
    run(repo, "checkout", "release/1.0.0")
    write(repo, "config/runtime.json", '{\n  "session_timeout": 30,\n  "retry_limit": 2,\n  "feature_flag": "tenant-safe-cache"\n}\n')
    write(repo, "dist/generated_config.json", '{"session_timeout":30,"retry_limit":2,"feature_flag":"tenant-safe-cache"}\n')
    return commit(repo, "release", "2026-06-23T10:00:00+05:30", "fix(config): add tenant safe cache flag and generated output"), []


def plant_missing_dependency_commit(repo: Path) -> tuple[str, list[str]]:
    run(repo, "checkout", "release/1.0.0")
    write(repo, "config/schema.py", 'REQUIRED_KEYS = ["tenant", "brand", "session_timeout"]\n')
    dependency_sha = commit(repo, "release", "2026-06-24T10:00:00+05:30", "feat(config): add tenant schema constants")
    write(repo, "src/auth/session.py", 'from config.schema import REQUIRED_KEYS\n\n\ndef validate_token(token, tenant):\n    if "tenant" not in REQUIRED_KEYS:\n        return False\n    if not token:\n        return False\n    return token.startswith(f"{tenant}:")\n')
    source_sha = commit(repo, "release", "2026-06-24T11:00:00+05:30", "fix(auth): validate token using tenant schema")
    return source_sha, [dependency_sha]


def plant_dirty_worktree(repo: Path) -> tuple[str, list[str]]:
    source_sha, deps = plant_clean_cherry_pick(repo)
    run(repo, "checkout", "tenant/tata-v1.0.0")
    write(repo, "tenants/tata/settings.json", '{\n  "tenant": "tata",\n  "brand": "Tata",\n  "dirty": true\n}\n')
    return source_sha, deps


PLANTERS = {
    "clean_cherry_pick": plant_clean_cherry_pick,
    "conflicting_cherry_pick": plant_conflicting_cherry_pick,
    "rewritten_history": plant_rewritten_history,
    "generated_files": plant_generated_files,
    "missing_dependency_commit": plant_missing_dependency_commit,
    "dirty_worktree": plant_dirty_worktree,
}


def write_json(path: Path, data: dict[str, Any]) -> None:
    path.write_text(json.dumps(data, indent=2, sort_keys=True) + "\n", encoding="utf-8")


def build_expected(
    scenario: dict[str, Any],
    repo: Path,
    base_sha: str,
    source_sha: str,
    dependency_shas: list[str],
) -> dict[str, Any]:
    target_branch = scenario["target_branch"]
    source_branch = scenario["source_branch"]
    run(repo, "checkout", target_branch)
    return {
        "schema_version": "1.0",
        "artifact_type": "repo_factory_expected",
        "scenario": scenario["name"],
        "description": scenario["description"],
        "created_at": "2026-06-01T00:00:00Z",
        "repo_path": str(repo),
        "branches": {
            "main": "main",
            "source": source_branch,
            "target": target_branch,
            "tenants": [
                "tenant/airtel-v1.0.0",
                "tenant/reliance-v1.0.0",
                "tenant/tata-v1.0.0",
            ],
        },
        "tenant": scenario["tenant"],
        "base_sha": base_sha,
        "source_branch_tip": git_out(repo, "rev-parse", source_branch),
        "target_branch_tip": git_out(repo, "rev-parse", target_branch),
        "source_shas": [source_sha],
        "dependency_shas": dependency_shas,
        "expected_result": scenario["expected_result"],
        "confidence": "high",
        "strategy": scenario["strategy"],
        "blockers": scenario["blockers"],
        "generated_files": scenario["generated_files"],
        "evidence_commands": [
            f"git checkout {target_branch}",
            "git status --short",
            f"git cherry-pick {source_sha}",
        ],
    }


def build_scenario(name: str, output: Path) -> Path:
    scenario = load_scenario(name)
    scenario_dir = output / name
    repo = scenario_dir / "repo"
    reset_dir(scenario_dir)
    base_sha = init_base_history(repo)
    source_sha, dependency_shas = PLANTERS[name](repo)
    write_json(scenario_dir / "scenario.json", scenario)
    expected = build_expected(scenario, repo, base_sha, source_sha, dependency_shas)
    expected_path = scenario_dir / "expected.json"
    write_json(expected_path, expected)
    write_json(
        scenario_dir / "manifest.json",
        {
            "scenario": name,
            "expected_sha256": hashlib.sha256(expected_path.read_bytes()).hexdigest(),
        },
    )
    return scenario_dir


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--scenario", help="Scenario name, or all.")
    parser.add_argument("--output", default="./output", help="Output directory.")
    parser.add_argument("--list", action="store_true", help="List available scenarios.")
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    if args.list:
        print("\n".join(available_scenarios()))
        return 0
    if not args.scenario:
        raise FactoryError("--scenario is required unless --list is used")

    names = available_scenarios() if args.scenario == "all" else [args.scenario]
    output = Path(args.output).resolve()
    output.mkdir(parents=True, exist_ok=True)

    print("Built fixtures:")
    for name in names:
        print(f"  {build_scenario(name, output)}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
