    write(repo, "src/billing/invoice.py", patch)
    source_sha = commit(repo, "release", "2026-06-22T10:00:00+05:30", "fix(billing): stabilize invoice rounding")
    run(repo, "checkout", "tenant/tata-v1.0.0")
    write(repo, "src/billing/invoice.py", patch)
    commit(repo, "tata", "2026-06-22T11:00:00+05:30", "fix(billing): apply Tata invoice rounding patch")
